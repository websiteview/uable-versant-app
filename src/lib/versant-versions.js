export const generateVersion = (versionId) => {
  const questions = [];
  let qId = 1;

  const partA = getSubset(repeatSentencesPool, versionId, 16);
  partA.forEach(item => questions.push({
      id: `pA_${qId++}`, type: 'repeat-sentence', section: 'Part A: Repeat',
      instruction: 'Please repeat each sentence exactly as you hear it.',
      sentence: item, timeLimit: 12, criteria: ['Sentence Mastery', 'Fluency', 'Pronunciation']
  }));

  const partB = getSubset(sentenceBuildsPool, versionId, 8);
  partB.forEach(item => questions.push({
      id: `pB_${qId++}`, type: 'sentence-build', section: 'Part B: Sentence Builds',
      instruction: 'Rearrange the word groups into a sentence.',
      parts: item.parts, correctSentence: item.correct, timeLimit: 24, criteria: ['Sentence Mastery', 'Fluency']
  }));

  const partC = getSubset(conversationsPool, versionId, 12);
  partC.forEach(item => questions.push({
      id: `pC_${qId++}`, type: 'conversation', section: 'Part C: Conversations',
      instruction: 'Listen to the conversation and answer the question.',
      audioText: `${item.text} ... Question: ${item.question}`,
      question: item.question, correctAnswer: item.answer, timeLimit: 26, criteria: ['Listening Comprehension']
  }));

  const partD = getSubset(completionSentencesPool, versionId, 18);
  partD.forEach(item => questions.push({
      id: `pD_${qId++}`, type: 'sentence-completion', section: 'Part D: Sentence Completion',
      instruction: 'Complete the sentence with the most appropriate word.',
      sentence: item.s, correctAnswer: item.a, timeLimit: 17, criteria: ['Reading', 'Vocabulary']
  }));

  const partE = getSubset(dictationPool, versionId, 14);
  partE.forEach(item => questions.push({
      id: `pE_${qId++}`, type: 'dictation', section: 'Part E: Dictation',
      instruction: 'Type the sentence exactly as you hear it.',
      sentence: item, timeLimit: 35, criteria: ['Listening', 'Writing']
  }));

  const partF = getSubset(passagesPool, versionId, 8);
  partF.forEach(item => questions.push({
      id: `pF_${qId++}`, type: 'passage-reconstruction', section: 'Part F: Passage Reconstruction',
      instruction: 'Read the passage. It will disappear after 30 seconds. Then reconstruct it in your own words.',
      passage: item.t, keyPoints: item.k, timeLimit: 180, readTime: 30, criteria: ['Reading', 'Writing', 'Memory']
  }));

  return questions;
};