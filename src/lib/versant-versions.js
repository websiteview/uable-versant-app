// Helper to get consistent subsets based on version index
const getSubset = (sourceArray, versionIndex, count) => {
  const start = ((versionIndex - 1) * count) % sourceArray.length;
  if (start + count <= sourceArray.length) {
    return sourceArray.slice(start, start + count);
  } else {
    return [...sourceArray.slice(start), ...sourceArray.slice(0, count - (sourceArray.length - start))];
  }
};

